# 假设主程序从地址0x000开始
# 主程序第一条指令地址为0x000
addi x5, x0, 100        # 地址 0x000 - 将100加载到寄存器x5

# 主程序第二条指令地址为0x004
jal x1, 12              # 地址 0x004 - 跳转到地址0x010的子程序，保存返回地址到x1

# 主程序第三条指令地址为0x008
addi x5, x5, 1          # 地址 0x008 - 当从子程序返回时执行，将x5增加1

# 子程序开始于此（硬编码的目标地址0x010）
addi x6, x0, 200        # 地址 0x010 - 将200加载到寄存器x6

# 子程序第二条指令地址为0x014
jalr x0, x1, 0          # 地址 0x014 - 返回到主程序的下一条指令地址0x008
